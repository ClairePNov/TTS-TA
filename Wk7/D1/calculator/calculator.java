public class calculator {
    
    //add variable that takes in 2 integer numbers and adds them together
    public int add(int num1, int num2) {
        return num1 + num2;
    }

    //subtract variable that takes in 2 integer numbers and subtracts them together   
    public int subtract(int num1, int num2) {
        return num1 - num2;
    }

    //multiply variable that takes in 2 integer numbers and multiplies them together
    public int multiply(int num1, int num2) {
        return num1 * num2;
    }

    //divide variable that takes in 2 integer numbers and divides them together
    public int divide(int num1, int num2) {
        return num1 / num2;
    }

    //square variable that takes in 1 integer number and squares it
    public int square(int num1) {
        return num1 * num1;
    }
}
